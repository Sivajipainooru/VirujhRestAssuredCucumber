package Pojo;

public class widgetImage {
	private String debug;
	private windowImage widow;
	private imageImage image;
	private textImage text;
	
	public String getDebug() {
		return debug;
	}
	public void setDebug(String debug) {
		this.debug = debug;
	}
	public windowImage getWidow() {
		return widow;
	}
	public void setWidow(windowImage widow) {
		this.widow = widow;
	}
	public imageImage getImage() {
		return image;
	}
	public void setImage(imageImage image) {
		this.image = image;
	}
	public textImage getText() {
		return text;
	}
	public void setText(textImage text) {
		this.text = text;
	}
	

}
